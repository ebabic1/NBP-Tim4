-- =============================================
-- AUDIT LOG TRIGGERS (log INSERT/UPDATE/DELETE to NBP_LOG)
-- =============================================

CREATE OR REPLACE TRIGGER NBP_ROLE_AUDIT
AFTER INSERT OR UPDATE OR DELETE ON NBP_ROLE
FOR EACH ROW
DECLARE
    v_action VARCHAR2(20);
BEGIN
    IF INSERTING THEN v_action := 'INSERT';
    ELSIF UPDATING THEN v_action := 'UPDATE';
    ELSE v_action := 'DELETE';
    END IF;
    INSERT INTO NBP_LOG (ID, ACTION_NAME, TABLE_NAME, DATE_TIME, DB_USER)
    VALUES (NBP_LOG_SEQ.NEXTVAL, v_action, 'NBP_ROLE', SYSTIMESTAMP, USER);
END;
/

