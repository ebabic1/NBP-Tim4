-- V3: Create auto-ID triggers and audit log triggers

-- =============================================
-- AUTO-ID TRIGGERS (assign sequence NEXTVAL on INSERT)
-- =============================================

CREATE OR REPLACE TRIGGER NBP_ROLE_BI
BEFORE INSERT ON NBP_ROLE FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        :NEW.ID := NBP_ROLE_SEQ.NEXTVAL;
    END IF;
END;
/

